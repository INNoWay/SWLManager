            String query;
                        
            senha1 = jPasswordField1.getText();
            
            senha2 = jPasswordField2.getText();

            query = "INSERT INTO func(nome, tel, cel, rua, n, cep, cidade, estado, cpf, rg, email, funcao, setor, login, senha, adm) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                        
